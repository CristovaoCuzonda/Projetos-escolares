    private void AgendarConsulta(){
    
        try {
            ConsultaC c = new ConsultaC();
            ConsultaBD cBd = new ConsultaBD();
            
            c.setEspecialidade(""+comboEspecialidade.getSelectedItem());
            c.setCodFuncionario(""+comboMedico.getSelectedItem());
            cBd.agendarConsulta(c);
            
            
        } catch (Exception e) {
        }
    }
    
    
    private void ListarConsulta() {

        getContentPane().setBackground(new Color(30, 30, 30));
        DefaultTableModel mode1 = (DefaultTableModel) tabelaConsulta.getModel();
        mode1.setNumRows(0);
        ConsultaBD bd = new ConsultaBD();
        ArrayList<ConsultaC> lista = (ArrayList<ConsultaC>) bd.listarConsultas();

        for (int i = 0; i < lista.size(); i++) {

            mode1.addRow(new Object[]{
                lista.get(i).getId_consulta(),
                lista.get(i).getBiPaciente(),
                lista.get(i).getCodFuncionario(),
                 lista.get(i).getEspecialidade(),
                  lista.get(i).getDataConsulta(),
                   lista.get(i).getHoraConsulta(),
                     lista.get(i).getSituacao(),
                      lista.get(i).getEstado()

                 
            
            });

        }
    }

    
    private void ListarComboBox(){
        PacienteBD b = new PacienteBD();
        ArrayList<PacienteC> lista = b.ListarPacientes();
        
        
        for (int i = 0; i < lista.size(); i++) {
            comboPaciente.addItem(lista.get(i).getBi());
        }
    
    }
     private void ListaComboMedico(){
           MedicoBD fBD = new MedicoBD();
        ArrayList<MedicoC> lista = (ArrayList<MedicoC>)fBD.listarMedicos();
        
        
         for (int i = 0; i < lista.size(); i++) {
            comboMedico.addItem(lista.get(i).getCodFuncionario());
        }
    }
   
     private void ListaComboEspecialidade(){
        EspecialidadeBD espBD = new EspecialidadeBD();
        ArrayList<EspecialidadeC> lista = espBD.ListarEspecialidades();
        
        
         for (int i = 0; i < lista.size(); i++) {
            comboEspecialidade.addItem(lista.get(i).getNome()+"");
        }
    }
    private void ListarValores() {
        try {
            PacienteBD bd = new PacienteBD();
            DefaultTableModel mode = (DefaultTableModel) tabelaPaciente.getModel();
            mode.setNumRows(0);

            ArrayList<PacienteC> lista = bd.ListarPacientes();
            for (int i = 0; i < lista.size(); i++) {

                mode.addRow(new Object[]{
                    lista.get(i).getId(),
                    lista.get(i).getBi(),
                    lista.get(i).getNome(),
                    lista.get(i).getNacionalidade(),
                    "Activo"
                });
                

            }
        } catch (Exception erro) {
        }

    }


