    private void ActualizarEspecialidade() {
        if (txtNomeActualizar.getText().equals("")) {
            JOptionPane.showConfirmDialog(null, "Não podemos actualizar dados");

        } else {
            EspecialidadeBD espBd = new EspecialidadeBD();
            EspecialidadeC esp = new EspecialidadeC();
            esp.setIdEspecialidade(Integer.parseInt((String)comboIdActualizar.getSelectedItem()));
            esp.setNome(txtNomeActualizar.getText());
            if (txtDescricaoactualizar.getText().isEmpty() == false) {
                esp.setDescricao(txtDescricaoactualizar.getText());
            }
            espBd.ActualizarEspecialidade(esp);

        }
    }

